package com.xworkz.countryinfo;

public class CountryInfo {
	String name;
	String pm;
	String notionalAnimal;
	
	public String getname() {
		return name;
	}
	public String getpm() {
		return pm;
	}
	public String getNotionalAnimal() {
		return notionalAnimal;
		
	}
	
	public void setname(String name) {
		this.name=name;
	}
	public void setPm(String pm) {
		this.pm=pm;
	}
	public void setNotionalAnimal(String notionalAnimal) {
		this.notionalAnimal=notionalAnimal;
	}

}
