package com.xworkz.typecastingtypeconverting;

public class Casting3 {
	
	//main method
	public static void main(String[] args) {
		
		//typeCasting
		float a;
		double b=4.8;
		a=(float)b;
		
		System.out.println(a);
		
		//typeConverting
		float c=10.00f;
		double d;
		d=c;
		
		System.out.println(d);
		
	
		
	}

}
