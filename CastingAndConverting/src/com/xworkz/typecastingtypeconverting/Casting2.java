package com.xworkz.typecastingtypeconverting;

public class Casting2 {
	
	//main method
	public static void main(String[] args) {
		
		

		//typeCasting
		float a;
		double b=4.8;
		a=(float)b;
		
		System.out.println(a);
		
		//typeConverting
		float c=30.01f;
		double d;
		d=c;
		System.out.println(d);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
