package com.xworkz.typecastingtypeconverting;

public class Casting4 {
	
	
	//main method
	public static void main(String[] args) {
		
		

		//typeCasting
		double a=6.8;
		int b;
		b=(int)a;
		
		System.out.println(a);
		
		//typeConverting
		short c;
		byte d=30;
		c=d;
		System.out.println(c);
		
	}

}
