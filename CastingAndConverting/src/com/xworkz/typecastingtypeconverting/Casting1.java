package com.xworkz.typecastingtypeconverting;

public class Casting1 {
	
	//main method
	public static void main(String[] args) {
		
		
		//typeCasting
		short a;
		int b=30;
		a=(short)b;
		
		System.out.println(a);
		
		///typeConverting
		short c=40;
		int d;
		d=c;
		
		System.out.println(d);
	}

}
